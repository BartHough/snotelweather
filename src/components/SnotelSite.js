export class SnotelSite extends Component {
    constructor(props) {
        super(props);
        this.state = {
            location: {
                lat: props.coords.lat,
                lng: props.coords.lng
            },
            days: props.days,
            url: 'http://api.powderlin.es/closest_stations?lat=' + props.coords.lat + '&lng=' + props.coords.lng + '&data=true&days=' + props.days + '&count=1',
            error: null,
            isLoaded: false,
            items: []
        }
    }


    componentDidMount() {
        alert(this.state.url)
        fetch(this.state.url)
            .then(res => res.json())
            .then(
                (result) => {
                    this.setState({
                        isLoaded: true,
                        items: result.items
                    });
                },
                // Note: it's important to handle errors here
                // instead of a catch() block so that we don't swallow
                // exceptions from actual bugs in components.
                (error) => {
                    this.setState({
                        isLoaded: true,
                        error
                    });
                }
            )
    }
    render() {
        const { error, isLoaded, items } = this.state;
        if (error) {
            return <div>Error: {error.message}</div>;
        } else if (!isLoaded) {
            return <div>Loading...</div>;
        } else {
            return (
                <ul>
                    {items.map(item => (
                        <li key={item.name}>
                            {item.name} {item.price}
                        </li>
                    ))}
                </ul>
            );
        }

    }
}

export default SnotelSite
